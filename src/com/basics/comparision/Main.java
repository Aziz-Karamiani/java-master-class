package com.basics.comparision;

public class Main {
    public static void main(String[] args) {
        // Comparison Operators < > <= >= == !=
        System.out.println(10 == 10);
        System.out.println(10 != 10);
        System.out.println(10 > 10);
        System.out.println(10 < 10);
        System.out.println(10 >= 10);
        System.out.println(10 <= 10);
    }
}
